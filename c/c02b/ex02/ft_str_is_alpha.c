int	ft_str_is_alpha(char *str)
{
	int	i;
	int	aux;

	i = 0;
	while (str[i] != '\0')
	{
		aux = str[i];
		if (!((aux >= 'A' && aux <= 'Z') || (aux >= 'a' && aux <= 'z')))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
